################################################################################
#   無限にldjsonのデータを生成し続けるスクリプト
#
#   Usage:
#     ruby $0 | head -n 10000
################################################################################

require "json"

i = 0
loop do
  item = {
    id: "id#{i}",
    name: "name#{i}",
    value: i,
  }
  puts JSON.dump(item)
  i += 1
end
